import numpy as np
from scipy import signal

PROJECT_ROOT = Path(__file__).resolve().parents[1]
C_HEADER_PATH = PROJECT_ROOT / "firmware" / "src" / "dsp" / "filter_coeffs.h"
PYTHON_FIXTURE_PATH = PROJECT_ROOT / "tests" / "fixtures" / "dsp_filters.py"

FS = 500.0
LOWCUT = 0.5
HIGHCUT = 40.0
BANDPASS_ORDER = 4
NOTCH_FREQ = 60.0
NOTCH_BW = 2.0


def design_bandpass_sos(fs: float, lowcut: float, highcut: float, order: int) -> np.ndarray:
    """Projeta bandpass Butterworth causal em SOS."""
    sos = signal.iirfilter(
        N=order,
        Wn=[lowcut, highcut],
        fs=fs,
        btype="band",
        ftype="butter",
        output="sos",
    )
    return sos


def design_notch_sos(fs: float, f0: float, bw: float) -> np.ndarray:
    """Projeta notch em SOS a partir de f0 e largura de banda (Hz)."""
    q = f0 / bw if bw > 0.0 else 1e6
    b, a = signal.iirnotch(w0=f0, Q=q, fs=fs)
    sos = signal.tf2sos(b, a)
    return sos


def sos_to_flat_coeffs(sos: np.ndarray) -> tuple[list[float], int]:
    """Converte SOS [b0,b1,b2,a0,a1,a2] -> flat [b0,b1,b2,a1,a2] por secao."""
    n_sections = sos.shape[0]
    flat: list[float] = []
    for row in sos:
        b0, b1, b2, a0, a1, a2 = row
        if not math.isclose(a0, 1.0, rel_tol=1e-9, abs_tol=1e-12):
            # Normaliza para a0 = 1.0 garantindo estabilidade numerica.
            b0, b1, b2 = b0 / a0, b1 / a0, b2 / a0
            a1, a2 = a1 / a0, a2 / a0
        flat.extend([float(b0), float(b1), float(b2), float(a1), float(a2)])
    return flat, n_sections


def format_c_array(flat: list[float], name: str, sections: int) -> str:
    """Formata array float para header C."""
    lines = [
        f"#define {name}_SECTIONS {sections}",
        f"static const float {name}_coeffs[{sections} * 5] = {{",
    ]
    for i in range(sections):
        base = i * 5
        coeffs = flat[base : base + 5]
        cstr = ", ".join(f"{c:.9e}f" for c in coeffs)
        lines.append(f"    /* section {i} */ {cstr},")
    lines.append("};")
    return "\n".join(lines)


def format_python_array(flat: list[float], sections: int, var_name: str) -> str:
    """Formata array numpy 2-D (sections x 5) para fixture Python."""
    lines = [f"{var_name}_FLAT = np.array(["]
    for i in range(sections):
        base = i * 5
        coeffs = flat[base : base + 5]
        cstr = ", ".join(f"{c:.9e}" for c in coeffs)
        lines.append(f"    # section {i}")
        lines.append(f"    {cstr},")
    lines.append("], dtype=np.float32)")
    lines.append(f"{var_name} = {var_name}_FLAT.reshape(-1, 5)")
    return "\n".join(lines)


def generate_c_header(
    bp_flat: list[float], bp_sections: int, notch_flat: list[float], notch_sections: int
) -> str:
    header = """/* AUTOGENERATED BY scripts/generate_filter_coeffs.py — DO NOT EDIT */
#ifndef LEWIS_FILTER_COEFFS_H
#define LEWIS_FILTER_COEFFS_H

#ifdef __cplusplus
extern "C" {{
#endif

/* Coeficientes IIR projetados em Python com SciPy (causal SOS).
 * Formato por secao: [b0, b1, b2, a1, a2]
 * Implementacao: transposed direct-form II biquad cascade.
 */

{bandpass}

{notch}

#ifdef __cplusplus
}}
#endif

#endif /* LEWIS_FILTER_COEFFS_H */
"""
    return header.format(
        bandpass=format_c_array(bp_flat, "LEWIS_BANDPASS", bp_sections),
        notch=format_c_array(notch_flat, "LEWIS_NOTCH", notch_sections),
    )


