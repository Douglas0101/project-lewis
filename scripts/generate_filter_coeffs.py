def main() -> int:
    parser = argparse.ArgumentParser(description="Gera coeficientes IIR para firmware DSP.")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Verifica se os arquivos gerados estao sincronizados (nao escreve).",
    )
    args = parser.parse_args()

    bp_sos = design_bandpass_sos(FS, LOWCUT, HIGHCUT, BANDPASS_ORDER)
    notch_sos = design_notch_sos(FS, NOTCH_FREQ, NOTCH_BW)

    bp_flat, bp_sections = sos_to_flat_coeffs(bp_sos)
    notch_flat, notch_sections = sos_to_flat_coeffs(notch_sos)

    c_header = generate_c_header(bp_flat, bp_sections, notch_flat, notch_sections)
    py_fixture = generate_python_fixture(bp_flat, bp_sections, notch_flat, notch_sections)

    # Normaliza formatacao do fixture Python para comparacao deterministica.
    try:
        import black

        py_fixture = black.format_str(py_fixture, mode=black.Mode(line_length=100))
    except ImportError:
        pass

    if args.check:
        c_existing = C_HEADER_PATH.read_text(encoding="utf-8") if C_HEADER_PATH.exists() else ""
        py_existing = (
            PYTHON_FIXTURE_PATH.read_text(encoding="utf-8") if PYTHON_FIXTURE_PATH.exists() else ""
        )
        if c_existing != c_header or py_existing != py_fixture:
            print("ERROR: filter coefficients are out of sync with generator", file=sys.stderr)
            return 1
        print("OK: filter coefficients are synchronized")
        return 0

    C_HEADER_PATH.parent.mkdir(parents=True, exist_ok=True)
    PYTHON_FIXTURE_PATH.parent.mkdir(parents=True, exist_ok=True)
    C_HEADER_PATH.write_text(c_header, encoding="utf-8")
    PYTHON_FIXTURE_PATH.write_text(py_fixture, encoding="utf-8")

    print(f"[generate] {C_HEADER_PATH}")
    print(f"[generate] {PYTHON_FIXTURE_PATH}")
    print(f"[info] bandpass sections={bp_sections}, notch sections={notch_sections}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
