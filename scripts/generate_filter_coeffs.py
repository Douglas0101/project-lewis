"""Gera coeficientes IIR para filtros DSP do firmware Project-Lewis.

Os filtros sao projetados deterministicamente com SciPy e exportados como:
  * firmware/src/dsp/filter_coeffs.h  (arrays const float para C)
  * tests/fixtures/dsp_filters.py     (referencia Python para testes)

Filtros:
  * Bandpass Butterworth 0.5-40 Hz, ordem 4, fs=500 Hz (causal SOS).
  * Notch 60 Hz, largura ~2 Hz (Q=30), fs=500 Hz (causal SOS).

A implementacao em C usa transposed direct-form II biquad cascade.
Os coeficientes sao armazenados como [b0, b1, b2, a1, a2] por secao.
"""

from __future__ import annotations

import argparse
import math
import sys
